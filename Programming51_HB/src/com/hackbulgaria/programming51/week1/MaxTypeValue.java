package com.hackbulgaria.programming51.week1;

public class MaxTypeValue {

	public static void main(String[] args) {
		int i = 0;
		
		for (; i >= 0; i++) ;
		
		System.out.println("Най-малкото число инт: " + i);
		System.out.println("Най-голямото число инт: " + (i - 1));
		
		/*
		 * Ще отнеме около 300 години :D
		 */
		long j = 0;
		
		for (; j >= 0; j++) ;
		
		System.out.println("Най-малкото число long: " + j);
		System.out.println("Най-голямото число long: " + (j - 1));

	}
	

}
